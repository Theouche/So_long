#include "so_long.h"

void	ft_free(char **mapbis)
{
	int	i;

	i = 0;
	if (!mapbis)
		return ;
	while (mapbis[i])
	{
		free(mapbis[i]);
		i++;
	}
	free(mapbis);
}
